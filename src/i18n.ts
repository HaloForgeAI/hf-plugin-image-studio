const translations = { en, zh };

function getLocale(): "en" | "zh" {
  const stored = window.localStorage.getItem("hf:locale");
  if (stored === "en" || stored === "zh") return stored;
  return navigator.language.toLowerCase().startsWith("zh") ? "zh" : "en";
}

export function useImageStudioT() {
  const locale = getLocale();
  const dict = translations[locale];
  return useCallback(
    (key: ImageStudioTranslationKey, vars?: Record<string, string | number>) => {
      const raw = dict[key] ?? en[key] ?? key;
      if (!vars) return raw;
      return Object.entries(vars).reduce<string>(
        (text, [name, value]) => text.split(`{${name}}`).join(String(value)),
        raw,
      );
    },
    [dict],
  );
}

export type ImageStudioT = ReturnType<typeof useImageStudioT>;
export type { ImageStudioTranslationKey };
